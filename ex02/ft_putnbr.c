#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	print_min(void)
{
	ft_putchar('-');
	ft_putchar('2');
	ft_putchar('1');
	ft_putchar('4');
	ft_putchar('7');
	ft_putchar('4');
	ft_putchar('8');
	ft_putchar('3');
	ft_putchar('6');
	ft_putchar('4');
	ft_putchar('8');
}

void	ft_putnbr(int nb)
{
	char	c;

	if (nb == -2147483648)
	{
		print_min();
		return ;
	}
	if (nb < 0)
	{
		c = '-';
		ft_putchar(c);
		nb *= -1;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		ft_putchar('0' + nb);
}
